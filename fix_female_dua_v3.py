with open("corrected_duas.txt", "w", encoding="utf-8") as f:
    for dua in duas:
        text = dua["arabic"]
        for old, new in replacements:
            # Create regex that matches word with optional prefixes (و, ف, ل, ب)
            # We capture the prefix in group 1
            # \b matches word boundary.
            # ([وفلب]?) matches optional prefix.
            # We use re.sub with a function or backreference.
            
            # Escape special chars in old just in case (though we have simple words)
            pattern = r"\b([وفلب]?)" + re.escape(old) + r"\b"
            
            # Replacement should keep the prefix
            # We can use \1 to refer to the captured prefix
            replacement = r"\1" + new
            
            text = re.sub(pattern, replacement, text)
        
        # Manual cleanups
        text = text.replace("فقيدتنا وموتى المسلمين", "فقيدتنا وموتى المسلمين")
        text = text.replace("فقيدتنا وموتي المسلمين", "فقيدتنا وموتى المسلمين")
        
        f.write(f'    {{ id: {dua["id"]}, category: "deceased-female", arabic: "{text}", transliteration: "{dua.get("transliteration", "")}", translation: "{dua.get("translation", "")}" }},\n')
